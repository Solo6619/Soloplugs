      <p class="article-intro">
        Il y a des plats qui nous parlent autrement. Qui nous disent : "Tu mérites qu'on prenne soin de toi." La salade tiède aux lentilles, chèvre et noix est de ceux-là. Ni too much, ni trop simple. Juste ce qu'il faut d'attention pour se faire vraiment plaisir.
      </p>

      <p>
        Ce n'est pas une salade qu'on mange parce qu'on "fait attention". C'est une salade qu'on choisit parce qu'elle nous fait du bien. Parce qu'après, on se sent nourri·e, satisfait·e, et un peu fier·ère d'avoir pris le temps de se composer une belle assiette.
      </p>

      <h2>L'élégance du quotidien</h2>

      <p>
        Cuisiner pour soi, c'est aussi s'autoriser l'élégance. Pas celle des grands restaurants, mais celle du quotidien choisi. Dresser joliment son assiette, même si personne ne la verra. Associer des saveurs qui nous plaisent, sans avoir à justifier nos goûts.
      </p>

      <p>
        Cette salade, c'est exactement ça : un petit moment d'élégance personnelle. Les lentilles vertes qui tiennent bien à la cuisson, le chèvre crémeux qui fond légèrement au contact de la chaleur, les noix qui apportent le croquant, la roquette qui donne du pep's. Chaque ingrédient a sa raison d'être.
      </p>

      <h2>Le plat qui dit "je vaux le coup"</h2>

      <p>
        Il y a quelque chose de symbolique à se préparer une salade composée. C'est dire : "Je ne suis pas en mode survival. Je prends le temps de me nourrir correctement, avec plaisir et attention."
      </p>

      <p>
        C'est important, ces petits gestes d'auto-bienveillance. Se préparer quelque chose de bon, de coloré, de nutritif. Ne pas se contenter du minimum, mais s'offrir ce petit supplément de soin qui fait la différence entre "manger" et "se nourrir".
      </p>

      <h2>L'art des associations</h2>

      <p>
        Cette salade nous apprend l'art des associations heureuses. Le chaud et le froid des lentilles tièdes sur la salade fraîche. L'onctueux et le croquant du chèvre et des noix. Le sucré et l'acidulé de la pomme et du vinaigre balsamique.
      </p>

      <p>
        Ces contrastes, c'est ce qui rend un plat vivant. Et quand on cuisine pour soi, on peut jouer avec ces équilibres selon notre humeur. Plus de chèvre aujourd'hui ? Quelques cranberries séchées si on en a ? Une poire à la place de la pomme ? C'est notre création.
      </p>

      <div class="recipe-box">
        <h3>Salade tiède lentilles-chèvre-noix</h3>
        <p><em>Pour 1 personne qui mérite une belle assiette</em></p>
        
        <div class="ingredients">
          <h4>Ingrédients :</h4>
          <ul>
            <li>100g de lentilles vertes cuites (conserve ou maison)</li>
            <li>50g de fromage de chèvre frais</li>
            <li>1 belle poignée de roquette (ou épinards)</li>
            <li>6-8 cerneaux de noix</li>
            <li>1/2 pomme (Granny Smith idéalement)</li>
            <li>1 cuillère à soupe de miel</li>
            <li>2 cuillères à soupe de vinaigre balsamique</li>
            <li>3 cuillères à soupe d'huile d'olive</li>
            <li>Sel, poivre</li>
          </ul>
        </div>
        
        <div class="method">
          <h4>Préparation :</h4>
          
          <p><strong>Préparer la vinaigrette :</strong> Dans un petit bol, mélanger le miel et le vinaigre balsamique. Monter à l'huile d'olive en émulsionnant à la fourchette. Goûter, ajuster avec une pincée de sel et de poivre. Cette vinaigrette peut se faire d'avance.</p>
          
          <p><strong>Réchauffer les lentilles :</strong> Si elles sortent d'une conserve, les rincer délicatement à l'eau tiède pour enlever le jus. Les réchauffer doucement dans une petite casserole avec un filet d'huile d'olive. Juste tiédir, pas bouillir. Assaisonner légèrement.</p>
          
          <p><strong>Préparer le reste :</strong> Laver et essorer la roquette. Couper la pomme en fines lamelles (garder la peau, c'est plus joli). Émietter le chèvre en gros morceaux. Concasser grossièrement les noix.</p>
          
          <p><strong>Dresser l'assiette :</strong> Disposer la roquette en lit dans une belle assiette. Répartir les lentilles tièdes par-dessus (elles vont légèrement flétrir les feuilles, c'est normal). Ajouter harmonieusement pomme, chèvre et noix.</p>
          
          <p><strong>La finition :</strong> Arroser de vinaigrette au moment de servir. Ne pas hésiter à bien napper tous les éléments. Finir par quelques tours de moulin à poivre.</p>
          
          <p><em>Cette salade se déguste immédiatement, en prenant le temps de savourer chaque bouchée. Les températures et les textures se mélangent en bouche. C'est un vrai plat, pas une punition santé.</em></p>
        </div>
      </div>

      <h2>Le rituel de la belle assiette</h2>

      <p>
        Préparer cette salade, c'est s'offrir un moment de slow food personnel. Prendre le temps de laver les feuilles une par une. De couper la pomme en jolies lamelles. De disposer chaque élément avec attention.
      </p>

      <p>
        Ces gestes nous ralentissent, nous ancrent dans l'instant. Ils nous rappellent que manger peut être un acte contemplatif, créatif, bienveillant envers soi-même. Pas juste une obligation nutritionnelle.
      </p>

      <h2>Quand la nutrition devient plaisir</h2>

      <p>
        Cette salade a tout bon nutritionnellement : protéines végétales, fibres, bons gras, vitamines. Mais surtout, elle a bon goût. Et c'est ça, le secret d'une alimentation durable quand on vit seul·e : trouver des plats qu'on fait par plaisir, pas par devoir.
      </p>

      <p>
        Quand on mange quelque chose qui nous fait vraiment plaisir, on se sent rassasié·e différemment. Satisfait·e pas seulement physiquement, mais aussi émotionnellement. C'est ça, bien se nourrir quand on est seul·e.
      </p>

