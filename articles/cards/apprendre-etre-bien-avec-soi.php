    <h3 class="article-item-title">Apprendre à être bien avec soi quand personne ne regarde</h3>
    <p class="article-item-excerpt">
        Pourquoi la solitude révèle ce que la vie sociale cache.
        Quand on vit seul, il n'y a plus de témoin pour valider nos choix, nos humeurs, notre façon d'être. 
        C'est déstabilisant. Mais c'est aussi là que commence une relation plus honnête avec soi-même.
