<!-- Section 1 - avec illustration chambre -->
<div class="suite-section clearfix">
  <img src="../images/ado-solo/harcelement-chambre.png" alt="Adolescent submergé par le cyberharcèlement" class="illustration-float-right">
  
  <p>Quand on te répète assez souvent que tu es « bizarre », « moche », « nul »… ton cerveau finit par se demander si ce n'est pas vrai.</p>
  
  <p>Tu commences à penser que si tu es harcelé, c'est forcément que tu as un problème.</p>
  
  <p class="emphase">C'est faux. Mais c'est normal de le ressentir.</p>
  
  <p>La solitude se transforme en dialogue interne très violent :</p>
  
  <div class="pensees-negatives">
    <span>« C'est de ma faute. »</span>
    <span>« Si j'étais différent, ça n'arriverait pas. »</span>
    <span>« Personne ne peut comprendre. »</span>
    <span>« Si j'en parle, on va dire que j'exagère. »</span>
  </div>
  
  <p>Et plus tu penses ça, plus tu te coupes des autres. Même de ceux qui pourraient t'aider.</p>
  
  <p>Tu peux avoir envie de disparaître : ne plus aller en cours, rester dans ta chambre, scroller des heures pour oublier. Tu peux aussi te mettre à douter de chaque personne gentille avec toi.</p>
  
  <p class="emphase">Si tu te reconnais dans ça, ce n'est pas que tu es faible. C'est que tu réagis à une situation qui te dépasse.</p>
</div>

<!-- Section 2 - avec illustration couloir -->
<div class="suite-section clearfix">
  <img src="../images/ado-solo/harcelement-couloir.png" alt="Adolescente isolée dans un couloir d'école" class="illustration-float-left">
  
  <h3>Tu n'es pas le problème</h3>
  
  <p>Ce qu'on ne te dit pas assez : le harcèlement parle plus de ceux qui harcèlent que de toi.</p>
  
  <p>Il y a des personnes qui ont besoin de rabaisser quelqu'un pour se sentir fortes, populaires, « drôles » devant les autres. Elles choisissent souvent une personne plus discrète, plus seule, plus « différente ». Pas parce qu'elle mérite ça — mais parce qu'elles la pensent plus facile à faire taire.</p>
  
  <p class="emphase">Ce que tu vis est injuste.</p>
  
  <p>Ce n'est pas une « étape normale » pour « s'endurcir ».<br>
  Ce n'est pas une preuve que tu vaux moins.<br>
  Ce n'est pas un test pour savoir si tu es assez fort.</p>
  
  <div class="droits">
    <p><strong>Tu as le droit d'être sensible.</strong></p>
    <p><strong>Tu as le droit d'avoir mal.</strong></p>
    <p><strong>Tu as le droit de dire que ça doit s'arrêter.</strong></p>
  </div>
</div>

<!-- Section 3 - Comment s'en sortir (avec image parcours) -->
<div class="suite-section">
  <h3>Comment commencer à t'en sortir</h3>
  
  <p>Tu n'as pas à régler tout ça tout seul. S'en sortir, ce n'est pas tout changer du jour au lendemain. C'est poser de petits gestes qui, mis bout à bout, ouvrent une porte.</p>
  
  <!-- Image parcours -->
  <img src="../images/ado-solo/harcelement-sen-sortir.png" alt="Les étapes pour s'en sortir : écrire, parler, trouver du soutien" class="illustration-parcours">
  
  <div class="conseil">
    <div class="conseil-contenu">
      <h4>Mettre des mots</h4>
      <p>Même si c'est juste pour toi au début, écrire ce que tu vis peut t'aider. Ce qui s'est passé, quand, où, qui était là. Ce que ça t'a fait ressentir.</p>
      <p>Ça te rappelle que ce sont des faits, pas des « impressions ». Et ça te donne des exemples concrets le jour où tu décides d'en parler.</p>
    </div>
  </div>

  <div class="conseil">
    <div class="conseil-contenu">
      <h4>En parler à quelqu'un</h4>
      <p>Quelqu'un qui n'est pas parfait, mais qui est fiable. Un parent, un grand frère ou une grande sœur. Un prof avec qui tu te sens un peu en sécurité. L'infirmière scolaire. Une ligne d'écoute.</p>
      <p class="exemple">« J'ai besoin de te parler d'un truc qui me fait mal depuis un moment, mais j'ai peur qu'on me dise que c'est rien. Est-ce que tu peux juste écouter jusqu'au bout ? »</p>
    </div>
  </div>

  <div class="conseil">
    <div class="conseil-contenu">
      <h4>Ne pas rester seul avec la honte</h4>
      <p>La honte fait croire qu'il faut se cacher. Le harcèlement adore le silence : tant que tu ne dis rien, ceux qui te font du mal se sentent libres de continuer.</p>
      <p>Parler, ce n'est pas balancer. C'est te protéger — et parfois aussi protéger d'autres qui vivent la même chose.</p>
    </div>
  </div>

  <div class="conseil">
    <div class="conseil-contenu">
      <h4>Définir tes limites</h4>
      <p>Bloquer les comptes qui t'attaquent. Quitter un groupe de discussion toxique. Refuser de rire d'un autre pour « être intégré ».</p>
      <p>Chaque fois que tu poses une limite, tu envoies un message : « je ne suis pas un punching-ball ».</p>
    </div>
  </div>

  <div class="conseil">
    <div class="conseil-contenu">
      <h4>Te créer des lieux sûrs</h4>
      <p>Tu ne peux pas changer les autres. Mais tu peux chercher des espaces où tu n'es pas défini par ce que tu subis. Une activité où tu te sens bien — sport, art, musique, écriture, jeux vidéo dans une communauté respectueuse.</p>
    </div>
  </div>
</div>

<!-- Section 4 - L'après -->
<div class="suite-section">
  <h3>Tu as le droit à un après</h3>
  
  <p>Peut-être que tu ne te vois pas plus loin que demain. Que tu te dis : « Ce sera comme ça jusqu'à la fin du secondaire. »</p>
  
  <p class="emphase">Mais ta vie ne se limitera pas à cette classe, à ces couloirs, à ces personnes.</p>
  
  <p>Il y aura d'autres lieux, d'autres gens, d'autres versions de toi. Des personnes qui ne connaissent pas l'histoire qu'on t'a collée sur le dos. Des endroits où ce qui fait que tu es différent aujourd'hui deviendra une force.</p>
</div>

<!-- Message final -->
<div class="message-fin">
  <p>Tu n'es vraiment pas seul à être seul, tu sais. La solitude, elle vit dans le silence de chacun. Il faut l'apprivoiser. Ou si tu veux que les autres l'entendent, il faut parler, écrire, dessiner, bouger. Trouver un langage pour l'exprimer et faire savoir que tu existes.</p>
  <p>Projette-toi dans le futur, crée-toi des rêves et accroche-toi à eux pour avancer.</p>
</div>

<!-- Alerte urgence -->
<div class="alerte-urgence">
  <p><strong>Si tu as des idées noires</strong>, si tu penses à te faire du mal ou à disparaître — ce n'est pas un caprice. C'est un signal d'alarme.</p>
  <p>Parle à un adulte. Contacte un service d'urgence. Une ligne d'écoute.</p>
  <p class="final"><strong>Ne laisse pas ce silence te faire du mal.</strong></p>
</div>
