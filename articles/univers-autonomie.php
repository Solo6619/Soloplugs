  <h1 class="article-title">Mon espace, mon territoire</h1>
  <p class="article-subtitle">Aménager un chez-soi pour une seule personne, c'est arrêter de meubler sa vie avec les goûts des autres.</p>
</header>

<article class="article-content">

  <p>La première chose que j'ai faite en emménageant seul, c'est acheter un canapé trop grand. Un réflexe. Comme si j'aménageais encore pour plusieurs, pour des invités hypothétiques, pour une vie qui n'était plus la mienne. Il a fallu des mois pour comprendre que cet appartement ne devait plaire à personne d'autre qu'à moi. Et que cette liberté-là, immense, était aussi vertigineuse qu'un mur blanc qu'il faut peindre soi-même.</p>

  <p>Aménager un espace pour une seule personne, ce n'est pas réduire. Ce n'est pas « faire avec moins ». C'est penser autrement. C'est comprendre qu'on n'a pas besoin d'une table de six quand on mange à un. Qu'un bureau peut devenir le centre du salon si c'est là qu'on passe le plus de temps. Que la chambre peut n'avoir qu'un lit et rien d'autre — un lit vaste, choisi avec soin, parce que dormir seul ne veut pas dire dormir mal.</p>

  <p class="highlight">Un chez-soi qui me ressemble, ce n'est pas un décor.<br>C'est un paysage intérieur devenu tangible.</p>

  <p>J'ai enlevé la deuxième table de nuit. J'ai remplacé l'étagère à bibelots par une planche de bois brut où je pose mes trois livres en cours et une plante. J'ai mis de la musique dans la salle de bain — pas un haut-parleur discret, un vrai système, parce que c'est dans cette pièce que je commence mes journées et que je veux qu'elles commencent bien. Chaque objet, maintenant, est là parce qu'il a un rôle dans ma vie. Pas dans celle que les autres imaginent pour moi.</p>

  <div class="separator">· · ·</div>

  <p>Il y a quelque chose de profondément libérateur dans le fait de choisir la couleur de ses murs sans négocier. De poser ses chaussures n'importe où sans commentaire. D'avoir un réfrigérateur à son image — parfois presque vide, parfois débordant, toujours sans devoir de compte. Ce sont des libertés minuscules, mais elles s'additionnent. Elles fabriquent, jour après jour, le sentiment de vivre chez soi. Vraiment chez soi.</p>

  <p>L'autonomie domestique, ce n'est pas que l'espace physique. C'est la gestion de tout ce qui tourne autour. Le budget qu'on tient seul, les factures qu'on paie seul, les décisions qu'on prend seul. C'est parfois lourd. C'est toujours formateur. Et c'est souvent, à la longue, une source inattendue de fierté. Savoir qu'on tient debout, par soi-même, dans un espace qu'on a construit de ses mains et de ses choix.</p>

  <p class="closing">Mon appartement n'est pas parfait. Il est petit, un peu sombre côté cuisine, et le voisin du dessus a le pas lourd. Mais c'est mon territoire. Le seul endroit au monde où chaque objet raconte quelque chose de moi.<br>Et quand je ferme la porte le soir, ce n'est pas le monde que j'enferme dehors — c'est moi que j'accueille dedans.</p>

</article>

